package com.flagfinder.enumeration;

/**
 * Enumeration representing the status of a friendship request.
 */
public enum FriendshipStatus {
    /**
     * Friendship request is pending approval.
     */
    PENDING,
    
    /**
     * Friendship request has been accepted.
     */
    ACCEPTED,
    
    /**
     * Friendship request has been declined.
     */
    DECLINE
}
