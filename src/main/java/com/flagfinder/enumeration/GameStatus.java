package com.flagfinder.enumeration;

/**
 * Enumeration representing the status of a game.
 */
public enum GameStatus {
    /**
     * Game is currently in progress.
     */
    IN_PROGRESS,
    
    /**
     * Game has been completed.
     */
    COMPLETED,
    
    /**
     * Game has been cancelled.
     */
    CANCELLED
}
